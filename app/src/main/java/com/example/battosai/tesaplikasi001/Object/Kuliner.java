public class Kuliner {

    int id;
    String nama_kuliner, alamat_kuliner, telpon_kuliner, ket_kuliner,gambar_kuliner;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama_kuliner() {
        return nama_kuliner;
    }

    public void setNama_kuliner(String nama_kuliner) {
        this.nama_kuliner = nama_kuliner;
    }

    public String getAlamat_kuliner() {
        return alamat_kuliner;
    }

    public void setAlamat_kuliner(String alamat_kuliner) {
        this.alamat_kuliner = alamat_kuliner;
    }

    public String getTelpon_kuliner() {
        return telpon_kuliner;
    }

    public void setTelpon_kuliner(String telpon_kuliner) {
        this.telpon_kuliner = telpon_kuliner;
    }

    public String getKet_kuliner() {
        return ket_kuliner;
    }

    public void setKet_kuliner(String ket_kuliner) {
        this.ket_kuliner = ket_kuliner;
    }

    public String getGambar_kuliner() {
        return gambar_kuliner;
    }

    public void setGambar_kuliner(String gambar_kuliner) {
        this.gambar_kuliner = gambar_kuliner;
    }
}
